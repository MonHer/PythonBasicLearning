#条件判断语句if

# if 语句基本使用法
price = 25
if price <= 20:
    print('buy cherry')
    print("cherry1")
    print("cherry2")
print('out of if')


# 为if添加else

price = 25
if price <= 20:
    print('buy cherry')
    print("buy apples")
else:
    print('buy grape')
    print("cherry2")
print('out of if')
